                        <h3>Description</h3>
                        <p id="recipe-description">Egg Manchurian is an another delicious recipe made by steaming the eggs and then deep frying them, the added sauces at the end nicely coats with the fried eggs and gives nice taste and sourness to the recipe .We can make it as a dry version or as a semi gravy type, egg Manchurian dry goes excellent with rice whereas gravy pairs with naan, roti and also with fried rice.
                            I have finely chopped the onions and capsicum to get nice texture , you can cut them into cubes if you prefer. Due to steaming process, the eggs will have more pores in it which absorbs more gravy inside, which can be followed for making many other recipes like egg gravy and egg curries. </p>

